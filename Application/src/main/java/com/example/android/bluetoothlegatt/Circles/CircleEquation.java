class CircleEquation
{
    public double x2;
    public double x;
    public double y2;
    public double y;
    public double num;

    public CircleEquation(double a, double b, double c, double d, double e) {
        x2 = a;
        x = b;
        y2 = c;
        y = d;
        num = e;
    }
}
